export function translate(locale: LocaleCode, key: TranslationKey, params: Record<string, string> = {}): string {
  const template = runtimeTranslationOverrides[locale]?.[key]
    ?? translations[locale]?.[key]
    ?? runtimeTranslationOverrides.en?.[key]
    ?? translations.en[key]
    ?? key;

  return Object.entries(params).reduce((value, [paramKey, paramValue]) => {
    return value.replaceAll(`{{${paramKey}}}`, paramValue);
  }, template);
}
